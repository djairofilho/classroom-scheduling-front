function getValue(locale, key) {
  return key.split('.').reduce((current, part) => current?.[part], translations[locale])
}

function interpolate(template, params) {
  if (typeof template !== 'string') return template
  return template.replace(/\{(\w+)\}/g, (_, token) => params?.[token] ?? `{${token}}`)
}

export function I18nProvider({ children }) {
  const [locale, setLocale] = useState(() => localStorage.getItem('portal-locale') ?? 'pt-BR')

  useEffect(() => {
    localStorage.setItem('portal-locale', locale)
  }, [locale])

  const value = useMemo(
    () => ({
      locale,
      setLocale,
      t(key, params) {
        const message = getValue(locale, key) ?? getValue('pt-BR', key) ?? key
        return interpolate(message, params)
      },
      tm(key) {
        return getValue(locale, key) ?? getValue('pt-BR', key) ?? key
      },
    }),
    [locale],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)

  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider')
  }

  return context
}
